	private class Canvas32x32 extends Canvas {
		
		private static final long serialVersionUID = 3277512952021171260L;

		BufferedImage rawDisplay;
		
		public Canvas32x32() {
			super();
			rawDisplay = new BufferedImage(XSIZE, YSIZE, BufferedImage.TYPE_INT_RGB);
			setSize(XSIZE, YSIZE);
		}
	
		public void paint( Graphics g ){

			for( int y = 0; y < 24; y++ )
				for( int x = 0; x < 40; x++ ) {
					int readValue = memory.getByte( getAddressLo40(((MemoryBusAppleIIe)memory).isPage2() ? 2:1, y, x));
					for( int yc = 0; yc < 16; yc++ )
						for( int xc = 0; xc < 14; xc++ )
							rawDisplay.setRGB((x*14)+xc, (y<<4)+yc, (TEXT_DISPLAY[readValue*8+(yc>>1)]&(0x01<<(xc>>1)))!=0 ? Color.WHITE.getRGB() : Color.BLACK.getRGB());
				}
			g.drawImage(rawDisplay, 0, 0, this);

		}
	
	}
	
